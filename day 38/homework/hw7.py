#შექმენით სია და გადაიყვანეთ tuple-ად tuple() ფუნქციის გამოყენებით, შემდეგ კი გადაიყვანეთ უკან list()-ით. დაბეჭდეთ ყველა შედეგი და დაუმატეთ კომენტარები რას აკეთებს თითოეული

list1 =["red", "blue", "yellow", "green"]
print(list1)

#tuple ჩვეულებრივ სიას გადააქცევს tuple - ად
list1 = tuple(list1)
print(list1)

#list tuple- ს გადააქცევს ჩვეულებრივ სიად
list1 = list(list1)
print(list1)